  <h3 class='text-center'>New Varieties for 2015</h3>
  <p class='text-center'>
    <a href="/luffa-ridged-edible-gourd-3-g-p-1834.html">
      <img style="height: 120px; margin: 2px;" alt="Mexican Mint Marigold"
           src="images/veg/ridge-gourds-on-cattle-panel-cropped.jpg" />
    </a>
    <a href="/jewels-of-opar-greens-015g-p-1824.html">
      <img style="height: 120px; margin: 2px;" alt="Jewels of Opar"
           src="/images/veg/jewels-of-opar.jpg" />
    </a>
    <a href="/charleston-hot-pepper-hot-05-g-p-1826.html">
      <img style="height: 120px; margin: 2px;" alt="Charleston Hot pepper"
           src="/images/veg/charleston-hot-pepper.jpg" />
    </a>
  </p>

  <p>
    For 2015 we're adding 29 heirloom and organic varieties to our listings.
    <a href="/jewels-of-opar-greens-015g-p-1824.html" target="_blank">Jewels of Opar</a>
    is both an elegant flower and a succulent summer leaf vegetable. Flavorful,
    attractive, bug-resistant <a href="/luffa-ridged-edible-gourd-3-g-p-1834.html" target="_blank">Ridge Gourds</a>
    can replace zucchini or okra in recipes. We've
    also added <a href="/charleston-hot-pepper-hot-05-g-p-1826.html" target="_blank">Charleston Hot</a>
    and <a href="/cayenne-carolina-pepper-hot-05-g-p-1825.html" target="_blank">Carolina Cayenne</a>,
    two hot peppers bred for resistance to nematodes, making them well-suited
    to the Southeast.
  </p>

  <p class='text-center'>
    <a href="/pennsylvania-dutch-crookneck-squash-winter-3-g-p-1793.html">
      <img style="margin: 2px; height: 120px;" alt="Pennsylvania Dutch Crookneck Winter Squash"
           src="/images/veg/pa-dutch-crookneck.jpg" />
    </a>
    <a href="/garden-huckleberry-009-g-p-1880.html">
      <img style="margin: 2px; height: 120px;" alt="Garden Huckleberries"
           src="/images/veg/garden-huckleberries.jpg" />
    </a>
    <a href="/index.php?main_page=advanced_search_result&search_in_description=1&keyword=24111">
      <img style="margin: 2px; height: 120px;" alt="Yellow Cabbage Collards"
           src="/images/veg/yellow-cabbage-collards.jpg" />
    </a>
  </p>

  <p>
    Keep eating from your garden all year with crops for canning, storage, and
    winter harvest. High-yielding
    <a href="/garden-huckleberry-009-g-p-1880.html" target="_blank">Garden Huckleberries</a>
    make a great jam.
    <a href="/pennsylvania-dutch-crookneck-squash-winter-3-g-p-1793.html" target="_blank">Pennsylvania Dutch Crookneck winter squash</a>
    is a butternut-line squash with excellent storage quality. Rare, tender
    family heirloom collard greens
    <a href="/alabama-blue-collards-2-g-p-1803.html" target="_blank">Alabama Blue</a>
    and
    <a href="/index.php?main_page=advanced_search_result&search_in_description=1&keyword=24111" target="_blank">Yellow Cabbage Collards</a>
    are traditional southern winter cooking greens.  You can also try
    <a href="/austrian-winter-peas-cover-crop-228-g-p-1813.html" target="_blank">Austrian Winter peas</a>
    for a great nitrogen-fixing cover crop that also adds flavor to salads all winter long in zones 7 and warmer.
  </p>

